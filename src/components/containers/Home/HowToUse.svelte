<h2>概要</h2>

<ul>
  <li>このアプリは 1:1 専用の映像通話アプリです。</li>
  <li>海外の方はご利用できません。</li>
  <li>同僚や友人とお使いください。不特定多数と繋がることはできません。</li>
</ul>

<h2>使い方</h2>

<ul>
  <li>
    まずは「カメラ・マイクチェック」ボタンを押し、自分の映像と音声の確認をしてください。
  </li>
  <li>
    音声がハウリングした場合、一度「退室する」ボタンを押し、スピーカーの音量を少し下げてから再度チェックをお願いします。
  </li>
  <li>準備が出来ましたら、片方が「ルーム生成」を押してください。</li>
  <li>ルームIDが生成・コピーされますので、そのIDを相手にお伝えください。</li>
  <li>上記テキスト欄に入力後、「入室する」ボタンをクリックしてください。</li>
  <li>カメラ・音声がONになった状態で開始されます。</li>
</ul>

