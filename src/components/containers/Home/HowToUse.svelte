<h2>使い方</h2>

<ul>
  <li>このアプリは 1:1 専用の映像通話アプリです。</li>
  <li>---</li>
  <li>まずは「ルーム作成」を押してください。</li>
  <li>作成後、ルームIDが表示されるので、そのIDを相手にお伝えください。</li>
  <li>
    IDを受け取った人は、上記テキスト入力欄に入れた後、「入室する」ボタンをクリックしてください。
  </li>
  <li>カメラ・音声がONになった状態で開始されます。</li>
  <li>---</li>
  <li>
    「カメラ・マイクチェック」ボタンから、自分の映像と音声の確認が行えます。音量が大きすぎると、ハウリングするので気を付けてください。
  </li>
</ul>
